"""Disk-image collector -- detects the image OS and runs Dissect queries."""
from __future__ import annotations

import logging
import re
from typing import Any

# --- Forensic enrichment helpers ------------------------------------------

# Living-Off-The-Land Binaries / Scripts -- legitimate signed Windows tools
# widely abused by attackers for defense evasion. Their presence alone is
# NOT suspicious (every Windows host runs these daily); we only flag them
# when context strengthens the signal -- persistence placement, suspicious
# argument patterns, or unusual parent process.
_LOLBAS: frozenset[str] = frozenset(x.lower() for x in [
    "certutil.exe", "regsvr32.exe", "bitsadmin.exe", "mshta.exe",
    "rundll32.exe", "wmic.exe", "cscript.exe", "wscript.exe",
    "installutil.exe", "powershell.exe", "powershell_ise.exe",
    "msbuild.exe", "msiexec.exe", "forfiles.exe", "scriptrunner.exe",
    "cmd.exe", "schtasks.exe", "psexec.exe", "psexesvc.exe", "ssh.exe",
])

# Paths that legitimate installers almost never write to -- binaries landing
# here nearly always indicate attacker staging or user-land persistence.
_SUSPICIOUS_PATH_PATTERNS: tuple[re.Pattern[str], ...] = tuple(
    re.compile(p, re.IGNORECASE) for p in [
        r"\\AppData\\Local\\Temp\\",
        r"\\AppData\\Roaming\\",
        r"\\Users\\Public\\",
        r"\\ProgramData\\(?!Microsoft\\|Package Cache\\|chocolatey\\)",
        r"\\Windows\\Temp\\",
        r"\\Recycle\.Bin\\",
        r"C:\\Temp\\", r"C:\\Tmp\\", r"C:\\Intel\\Logs\\",
    ]
)

# Location classes that are legitimate-by-design. These are the three
# STRUCTURAL signals a binary path can give us about operator intent -- all
# generalise across any Windows image without knowing a single binary name:
#
#   1. ``\\tsclient\`` -- an RDP client-side drive redirect. By definition
#      an interactive human is running a tool from their own workstation.
#      Attackers with pre-placed implants don't reach back through tsclient.
#
#   2. ``\Program Files\`` / ``\Program Files (x86)\`` / ``\Windows\``
#      (outside the Temp subtrees) -- Windows' canonical install roots.
#      Installers require integrity checks to write here; malware that
#      lands here has either already had admin, or signed MSI delivery --
#      either way, the Temp/AppData staging heuristic would have fired
#      upstream before the binary reached this directory.
#
#   3. Microsoft's own installer staging GUIDs under Temp
#      (``\{GUID\}~setup\``, ``\{GUID\}\.cr\``, ``\{GUID\}\.be\``) -- these
#      are generated by Windows Installer itself for MSI / Bootstrapper
#      extraction. The pattern is universal MS infrastructure, not
#      attacker-controlled; unpacking a legit MSI looks exactly like this
#      on every Windows host since Vista.
_BENIGN_PATH_PATTERNS: tuple[re.Pattern[str], ...] = tuple(
    re.compile(p, re.IGNORECASE) for p in [
        r"\\\\tsclient\\",
        r"\\\{[0-9A-Fa-f\-]{8,}\}~?setup\\",           # {GUID}~setup\ and {GUID}setup\
        r"\\\{[0-9A-Fa-f\-]{8,}\}\\\.(?:cr|be)\\",     # {GUID}\.cr\ and {GUID}\.be\
        r"C:\\Program Files(?: \(x86\))?\\(?!.*\\(Temp|Tmp)\\)",
        r"C:\\Windows\\(System32|SysWOW64|WinSxS|Microsoft\.NET|servicing|assembly)\\",
    ]
)

# Attacker-shaped argument fragments. A LOLBAS invocation with any of
# these in its arguments is genuinely suspicious regardless of placement.
_ATTACKER_ARG_PATTERNS: tuple[re.Pattern[str], ...] = tuple(
    re.compile(p, re.IGNORECASE) for p in [
        r"(?:^|[\s\"'])[-/]enc(?:odedcommand)?\b",    # powershell -enc <base64>
        r"\biex\b", r"\binvoke-expression\b",          # in-memory execution
        r"\bdownloadstring\b", r"\bdownloadfile\b",
        r"\bfrombase64string\b",
        r"\bbypass\b.*\bexecutionpolicy\b|\bexecutionpolicy\b\s+bypass",
        r"\bhidden\b.*\bwindowstyle\b|\bwindowstyle\s+hidden\b",
        r"\bnew-object\s+net\.webclient\b",
        r"\burlcache\b.*\bcertutil\b|\bcertutil\b.*\burlcache\b",
        r"(?:^|[\s\"'])[-/]nop(?:rofile)?\b",          # powershell -nop / -noprofile
        r"(?:^|[\s\"'])[-/]w(?:indowstyle)?\s+hidden",
        r"\bregsvr32\b.*\/i\s*:\s*http",
        r"\bmshta\b\s+http",
        r"\bfrombase64string\b",
    ]
)

# Location classes where a LOLBAS appearance IS suspicious on its own --
# these slots are rare and attacker-shaped. A bare ``rundll32.exe`` entry
# in ``tasks`` is NOT on this list because Windows itself creates hundreds
# of legit rundll32 tasks; tasks only flag when args add signal.
_STRONG_PERSISTENCE_KINDS: frozenset[str] = frozenset({
    "runkeys", "services", "startupinfo",
})


def _first_token(s: str) -> str:
    s = s.strip().strip('"').strip("'")
    if not s:
        return ""
    return s.split()[0]


def _basename(path: str) -> str:
    last = path.replace("/", "\\").rstrip("\\").rsplit("\\", 1)[-1]
    return _first_token(last).lower()


def _mark_suspicious(
    record: dict[str, Any],
    *,
    candidate_fields: tuple[str, ...],
    kind: str = "",
) -> dict[str, Any]:
    """Tag a record with ``suspicious_reasons`` when the record matches one
    of several context-aware heuristics. Designed to minimise false
    positives -- a plain ``powershell.exe`` prefetch entry no longer flags
    as "lolbas"; only when context says so (persistence placement,
    attacker-shaped args, or suspicious path).

    ``kind`` is the collector's artifact type (runkeys / services / tasks /
    startupinfo / prefetch / shellbags / …). Persistence kinds get a stricter
    LOLBAS rule; execution kinds get a looser one.
    """
    reasons: list[str] = []
    blob_parts: list[str] = []
    tokens: list[str] = []

    for f in candidate_fields:
        val = record.get(f)
        if val is None:
            continue
        if isinstance(val, dict):
            ex = str(val.get("executable") or "")
            blob_parts.append(ex)
            args = val.get("args") or []
            blob_parts.append(" ".join(str(a) for a in args))
            if ex:
                tokens.append(_basename(ex))
        else:
            blob_parts.append(str(val))
            tokens.append(_basename(str(val)))

    blob = " ".join(blob_parts).strip()
    if not blob:
        return record
    blob_l = blob.lower()

    # === Positive (attacker-shape) signals scanned FIRST ===
    # The benign path allow-list below only skips the record when no
    # positive signal has fired. A LOLBAS argv with `-enc` cannot be made
    # clean by the fact that its binary lives in System32.
    #
    # Pre-compute whether a benign-path pattern matches the blob. This is
    # used in two places: (a) the suspicious-path check below skips when
    # the same blob also matches a benign pattern (MSI GUID staging
    # directories sit INSIDE Windows\Temp but are legitimate), and (b) the
    # end-of-function short-circuit that drops benign records entirely.
    benign_path_hit = any(pat.search(blob) for pat in _BENIGN_PATH_PATTERNS)

    # Suspicious path -- attacker staging location (AppData\Temp, Windows
    # \Temp without a benign signature, Users\Public, ProgramData root…).
    if not benign_path_hit:
        for pat in _SUSPICIOUS_PATH_PATTERNS:
            if pat.search(blob):
                reasons.append(f"suspicious_path:{pat.pattern}")
                break

    # Attacker-shaped argument fragments are high-signal regardless of kind.
    arg_hit = False
    for pat in _ATTACKER_ARG_PATTERNS:
        if pat.search(blob):
            reasons.append(f"suspicious_arg:{pat.pattern}")
            arg_hit = True
            break

    # Double extension (e.g. invoice.pdf.exe) -- classic phishing dropper.
    if re.search(r"\.(pdf|doc|docx|xls|xlsx|jpg|png|txt)\.(exe|scr|bat|com|ps1|vbs)\b", blob_l):
        reasons.append("double_extension")

    # LOLBAS -- context-aware. A persistence slot (runkeys/services/
    # startupinfo) with any LOLBAS fires. Anywhere else, a LOLBAS only
    # fires when a positive path/arg signal has already been found.
    lolbas_hit: str | None = None
    for tok in tokens:
        if tok in _LOLBAS:
            lolbas_hit = tok
            break
    if lolbas_hit and (
        kind in _STRONG_PERSISTENCE_KINDS
        or arg_hit
        or any(r.startswith("suspicious_path:") for r in reasons)
    ):
        reasons.append(f"lolbas:{lolbas_hit}")

    # === Structural benign-path short-circuit ===
    # Only skip when no attacker-shape signal fired above. Binaries in
    # \\tsclient\, MS-installer GUID dirs, Program Files, or Windows system
    # directories are benign-by-default but NOT unconditionally.
    if not reasons and benign_path_hit:
        return record

    # De-dup while preserving order.
    seen: set[str] = set()
    deduped: list[str] = []
    for r in reasons:
        if r not in seen:
            seen.add(r)
            deduped.append(r)
    if deduped:
        record["suspicious_reasons"] = deduped
    return record

